#!/usr/bin/env bash
# benchmarks/sccache-setup.sh — v22 T5 (L35 Build performance).
# Authority: plans/2026-06-22-v22-71-pillar-cycle-12-p1.md Track T5.
# Idempotent fleet-wide sccache bootstrap: install + start service +
# write .cargo/env so every cargo invocation gets cache acceleration.
# Usage: bash benchmarks/sccache-setup.sh [--status|--stop]
set -euo pipefail

SCCACHE_BIN="${SCCACHE_BIN:-sccache}"
SCCACHE_DIR="${SCCACHE_DIR:-$HOME/.cache/sccache}"
SCCACHE_SIZE="${SCCACHE_SIZE:-10G}"
CARGO_ENV_DIR="${CARGO_HOME:-$HOME/.cargo}"
CARGO_ENV_FILE="$CARGO_ENV_DIR/env"

log() { printf '[sccache-setup] %s\n' "$*" >&2; }

install() {
  command -v "$SCCACHE_BIN" >/dev/null && { log "installed: $($SCCACHE_BIN --version)"; return; }
  case "$(uname -s)" in
    Linux)
      if   command -v apt-get >/dev/null; then sudo apt-get install -y -qq sccache
      elif command -v dnf     >/dev/null; then sudo dnf install -y sccache
      else log "no supported package manager"; exit 1; fi ;;
    Darwin) command -v brew >/dev/null && brew install sccache || { log "brew required"; exit 1; } ;;
    *) log "unsupported OS: $(uname -s)"; exit 1 ;;
  esac
}

start() {
  mkdir -p "$SCCACHE_DIR"
  pgrep -f "sccache --start-server" >/dev/null || \
    SCCACHE_DIR="$SCCACHE_DIR" SCCACHE_CACHE_SIZE="$SCCACHE_SIZE" "$SCCACHE_BIN" --start-server
  log "service running (size=$SCCACHE_SIZE)"
}

write_env() {
  mkdir -p "$CARGO_ENV_DIR"
  cat > "$CARGO_ENV_FILE" <<EOF
# Generated by benchmarks/sccache-setup.sh on $(date -u +%FT%TZ)
export SCCACHE_BIN="$SCCACHE_BIN"
export SCCACHE_DIR="$SCCACHE_DIR"
export SCCACHE_CACHE_SIZE="$SCCACHE_SIZE"
export RUSTC_WRAPPER="$SCCACHE_BIN"
alias sccache-stats="$SCCACHE_BIN --show-stats"
EOF
  log "wrote $CARGO_ENV_FILE (source it, or use direnv to auto-load)"
}

status() { command -v "$SCCACHE_BIN" >/dev/null && "$SCCACHE_BIN" --show-stats || { log "not installed"; exit 1; }; }
stop()   { command -v "$SCCACHE_BIN" >/dev/null && "$SCCACHE_BIN" --stop-server || true; log "stopped"; }

case "${1:-install}" in
  --status) status ;;
  --stop)   stop ;;
  *)        install; start; write_env ;;
esac
