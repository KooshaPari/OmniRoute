def strip_ansi(s: str) -> str:
    return re.sub(r"\x1b\[[0-9;]*m", "", s)


def gh_api(path: str):
    try:
        r = subprocess.run(["gh", "api", path], capture_output=True, text=True, timeout=30)
        if r.returncode != 0 or not r.stdout.strip():
            return None
        return json.loads(strip_ansi(r.stdout))
    except Exception:
        return None


def gh_json_list(path: str) -> list:
    try:
        r = subprocess.run(
            ["gh", "api", path, "--paginate"],
            capture_output=True,
            text=True,
            timeout=60,
        )
        if r.returncode != 0 or not r.stdout.strip():
            return []
        clean = strip_ansi(r.stdout)
        try:
            data = json.loads(clean)
            return data if isinstance(data, list) else [data]
        except json.JSONDecodeError:
            chunks = re.findall(r"\[[^\[\]]*\]", clean)
            combined = []
            for c in chunks:
                try:
                    combined.extend(json.loads(c))
                except json.JSONDecodeError:
                    pass
            return combined
    except Exception:
        return []


def fetch_repo_meta(repo: str) -> dict:
    meta = gh_api(f"repos/{repo}")
    if not meta:
        return {}
    branches_raw = gh_json_list(f"repos/{repo}/branches?per_page=100")
    branches = [
        {"name": b.get("name", "?"), "sha": (b.get("commit", {}).get("sha") or "")[:7]}
        for b in branches_raw
    ]
    prs = gh_json_list(f"repos/{repo}/pulls?state=all&per_page=100")
    return {
        "slug": repo.split("/", 1)[1],
        "owner": repo.split("/", 1)[0],
        "full_name": meta.get("full_name", repo),
        "html_url": meta.get("html_url", ""),
        "default_branch": meta.get("default_branch", "main"),
        "visibility": meta.get("visibility", "?"),
        "archived": meta.get("archived", False),
        "size_kb": meta.get("size", 0),
        "stargazers": meta.get("stargazers_count", 0),
        "open_issues": meta.get("open_issues_count", 0),
        "created_at": meta.get("created_at", "")[:19],
        "pushed_at": (meta.get("pushed_at") or "")[:19],
        "updated_at": (meta.get("updated_at") or "")[:19],
        "description": meta.get("description") or "(none)",
        "language": meta.get("language") or "(none)",
        "license": (meta.get("license") or {}).get("spdx_id", "(none)"),
        "branches": branches,
        "branch_count": len(branches),
        "pr_count": len(prs),
    }


