def main() -> int:
    today = datetime.now().strftime("%Y-%m-%d")
    written = []
    for repo in REPOS:
        meta = fetch_repo_meta(repo)
        if not meta:
            print(f"  [SKIP] {repo}: gh api returned no data", file=sys.stderr)
            continue
        body = render_audit(meta, today)
        if not body:
            continue
        slug = meta["slug"]
        out_path = AUDITS_DIR / f"{slug}-{today}.md"
        out_path.write_text(body, encoding="utf-8")
        print(f"  [OK]   {repo} -> {out_path.name} ({len(body)} bytes, {meta['branch_count']} branches)")
        written.append(out_path)

    if not written:
        print("No audits written.", file=sys.stderr)
        return 1

    print()
    print("=" * 60)
    print("Re-grading fleet with all 13 audits")
    print("=" * 60)
    r = subprocess.run(
        ["bash", str(GRADER.parent / "_grade-all.sh")],
        capture_output=True,
        text=True,
    )
    print(r.stdout)
    if r.returncode != 0:
        print(r.stderr, file=sys.stderr)
    return r.returncode


if __name__ == "__main__":
    sys.exit(main())
