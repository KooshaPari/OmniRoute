import { writeFile } from "node:fs/promises";
import os from "node:os";

import inventory from "../../../config/performance/v4-latency-inventory.json";
import {
  BenchmarkReportSchema,
  assertUnique,
  inventorySha256,
} from "../src/observability/benchmark-contract";
import { prepareBenchmarkOutputPath } from "../src/observability/benchmark-output-path";
import { summarizeLatencySamples, type LatencySampleV1 } from "../src/observability/latency";
import { installBenchmarkEgressGuard } from "../src/observability/benchmark-egress-guard";

const sourceCommit = process.env.SOURCE_COMMIT;
const sourceTree = process.env.SOURCE_TREE;
if (!sourceCommit || !/^[0-9a-f]{40}$/.test(sourceCommit)) throw new Error("invalid SOURCE_COMMIT");
if (!sourceTree || !/^[0-9a-f]{40}$/.test(sourceTree)) throw new Error("invalid SOURCE_TREE");
const output = process.argv[2];
if (!output) throw new Error("usage: bun scripts/benchmark-latency.ts <output.json>");
const outputPath = await prepareBenchmarkOutputPath(output);

// Install process-local direct socket/DNS guards before loading the built application.
const egressGuard = installBenchmarkEgressGuard();
// Benchmark the artifact produced by the preceding build step, not a second source import.
// @ts-expect-error dist/index.js is generated by `bun run build`.
const { default: app } = await import("../dist/index.js");
if (!app?.routes || typeof app.fetch !== "function") throw new Error("built BFF export is invalid");

const registeredRoutes = [...new Set<string>(app.routes.map((entry: { method: string; path: string }) =>
  `${entry.method} ${entry.path}`))].sort((left, right) => left.localeCompare(right, "en"));
assertUnique(registeredRoutes, "route inventory");
const routeInventoryHash = inventorySha256(registeredRoutes);
if (registeredRoutes.length !== inventory.registeredRouteCount ||
    routeInventoryHash !== inventory.registeredRouteSha256) {
  throw new Error(`registered route inventory drift: ${registeredRoutes.length} ${routeInventoryHash}`);
}

const measuredRoutes = inventory.benchmarkedRoutes;
assertUnique(measuredRoutes.map((entry) => `${entry.method} ${entry.path}`), "measured routes");
for (const route of measuredRoutes) {
  if (!registeredRoutes.includes(`${route.method} ${route.path}`)) {
    throw new Error(`unregistered measured route: ${route.method} ${route.path}`);
  }
}

const server = Bun.serve({ hostname: "127.0.0.1", port: 0, fetch: app.fetch });
if (!server.port) throw new Error("loopback server did not allocate a port");
const origin = `http://127.0.0.1:${server.port}`;
egressGuard.setAllowedLoopbackPort(server.port);

const warmupCount = 30;
const sampleCount = 250;
async function request(route: { path: string; method: string }) {
  const start = Bun.nanoseconds();
  const response = await fetch(`${origin}${route.path}`, { method: route.method });
  await response.arrayBuffer();
  return { durationMs: (Bun.nanoseconds() - start) / 1_000_000, status: response.status };
}

const results = [];
try {
  for (const route of measuredRoutes) {
    for (let sequence = 0; sequence < warmupCount; sequence++) await request(route);
    const samples = [];
    const aggregateInput: LatencySampleV1[] = [];
    for (let sequence = 0; sequence < sampleCount; sequence++) {
      const measured = await request(route);
      samples.push({ sequence, ...measured });
      aggregateInput.push({
        schemaVersion: 1,
        route: route.path,
        method: route.method as LatencySampleV1["method"],
        ...measured,
      });
    }
    results.push({
      route: route.path,
      method: route.method,
      ...summarizeLatencySamples(aggregateInput),
      samples,
    });
  }
} finally {
  server.stop(true);
  egressGuard.restore();
}
const blockedNonLoopbackAttempts = egressGuard.blockedAttemptCount();
if (blockedNonLoopbackAttempts !== 0) throw new Error("non-loopback network access was attempted");

const report = BenchmarkReportSchema.parse({
  schemaVersion: 2,
  sourceCommit,
  sourceTree,
  benchmark: {
    transport: "bun-loopback-tcp-built-bff",
    hostname: "127.0.0.1",
    warmupCount,
    sampleCount,
    percentileMethod: "nearest-rank",
    errorDefinition: "HTTP status >= 500",
    clock: "Bun.nanoseconds",
  },
  networkPolicy: {
    mode: "deny-non-loopback-network",
    guardCoverage: egressGuard.installedCoverage(),
    guardActivations: egressGuard.activationResults(),
    allowedLoopbackAttempts: egressGuard.allowedLoopbackAttempts(),
    blockedNonLoopbackAttempts,
  },
  environment: {
    runtime: `bun ${Bun.version}`,
    runnerImage: process.env.RUNNER_IMAGE || "local-unpinned",
    platform: process.platform,
    architecture: process.arch,
    cpuCount: os.cpus().length,
  },
  rssBytes: process.memoryUsage.rss(),
  routeInventory: {
    count: registeredRoutes.length,
    sha256: routeInventoryHash,
    routes: registeredRoutes,
  },
  routes: results,
});

await writeFile(outputPath, `${JSON.stringify(report, null, 2)}\n`, { mode: 0o600 });
console.log(`wrote ${outputPath} for ${sourceCommit}`);
